#include <iostream>

using namespace std;

int main () {

    cout << "Size of char: " << sizeof(char) << "\n";
    cout << "Size of bool: " << sizeof(bool) << "\n";
    cout << "Size of short: " << sizeof(short) << "\n";
    cout << "Size of int: " << sizeof(int) << "\n";
    cout << "Size of std::string: " << sizeof(std::string) << "\n";
};
